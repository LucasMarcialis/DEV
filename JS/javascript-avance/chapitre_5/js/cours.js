// Incrémentation
i = 0;
number = ++i;
console.log(i); // Affiche 1
console.log(number); // Affiche 1

// Mais aussi
i = 0;
number = i++;
console.log(i); // Affiche 1
console.log(number); // Affiche 0

// Décrémentation
i = 0;
number = i--;
console.log(i); // Affiche -1
console.log(number); // Affiche 0


i = 0;
number = --i;
console.log(i); // Affiche -1
console.log(number); // Affiche -1

